package java200102;

public class Datatype_1 {

	public static void main(String[] args) {
		// boolean 변수를 만들어서 값을 저장하기
		boolean isNum = false;
		// 변수의 값을 출력해보기
		System.out.println("isNum:" + isNum);
		
		//task 내 나이를 저장하고 출력해보자!
		int age = 31;
		// 나이 변수를 출력하기
		System.out.println("age: "+ age);

		//int와 char로 a를 저장해서 출력값 비교해보기
		char ch = 'a';
		int c = 'a';
		System.out.println("ch:" + ch);
		System.out.println("c:" +c);
		
		// 자신의 키와 몸무게를 저장할 수 있는 변수를 만들어서 저장한 후 출력
		double h = 173.0;
		double w = 58.8;
		System.out.println("my height is :" + h +' '+ "my weight is :" + w);
		
	}

}
